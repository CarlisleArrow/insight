const fs = require('fs');
const topo = require('world-atlas/countries-110m.json');

// Full world basemap for the Federation "Federated footprint" choropleth.
// We keep every country so the whole world renders; Antarctica is dropped so the
// projection fits to the inhabited world instead of a huge empty polar band.
// The lit countries (where plants live) are decided at runtime in choropleth.js
// from FED_PLANTS — this file only carries the static basemap geometry.
const DROP_IDS = new Set(['010']); // Antarctica

const geoms = topo.objects.countries.geometries.filter((g) => !DROP_IDS.has(g.id));

// --- re-index arcs so we only keep the ones the kept geometries reference ---
const used = new Map(); // oldAbsIndex -> newIndex
const remapRing = (ring) => ring.map((a) => {
  const abs = a < 0 ? ~a : a;
  if (!used.has(abs)) used.set(abs, used.size);
  const ni = used.get(abs);
  return a < 0 ? ~ni : ni;
});
const remapArcs = (g) => {
  if (g.type === 'Polygon') g.arcs = g.arcs.map(remapRing);
  else if (g.type === 'MultiPolygon') g.arcs = g.arcs.map((poly) => poly.map(remapRing));
};

const newGeoms = geoms.map((g) => {
  const ng = { type: g.type, id: g.id, arcs: JSON.parse(JSON.stringify(g.arcs)),
    properties: { name: g.properties.name, NAME: g.properties.name } };
  remapArcs(ng);
  return ng;
});
const newArcs = new Array(used.size);
for (const [oldAbs, ni] of used) newArcs[ni] = topo.arcs[oldAbs];

const worldTopo = {
  type: 'Topology',
  transform: topo.transform,
  arcs: newArcs,
  objects: { countries: { type: 'GeometryCollection', geometries: newGeoms } },
};

const banner = '// AUTO-GENERATED by scripts/gen-world-choropleth.cjs (world-atlas countries-110m, minus Antarctica). Do not edit by hand.\n';
fs.writeFileSync('src/data/worldGeo.js',
  banner + 'export const WORLD_TOPO = ' + JSON.stringify(worldTopo) + ';\n');

console.log('wrote src/data/worldGeo.js —', newGeoms.length, 'countries,', newArcs.length, 'arcs');
